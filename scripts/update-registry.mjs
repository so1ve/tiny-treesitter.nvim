import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const registryBaseUrl = process.env.REGISTRY_SOURCE_URL ?? "https://raw.githubusercontent.com/arborist-ts/arborist.nvim/main/registry";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");

function log(message) {
  console.log(`[update-registry] ${message}`);
}

async function fetchText(name) {
  const url = `${registryBaseUrl.replace(/\/$/, "")}/${name}`;

  log(`fetching ${url}`);

  const response = await fetch(url);

  if (!response.ok) {
    throw new Error(`failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.text();
}

function parseScalar(value) {
  const trimmed = value.trim();

  if (trimmed === "true") {
    return true;
  }

  if (trimmed === "false") {
    return false;
  }

  if (trimmed.startsWith('"') && trimmed.endsWith('"')) {
    return trimmed.slice(1, -1);
  }

  return trimmed;
}

function parseArray(value) {
  const body = value.trim().slice(1, -1).trim();

  if (!body) {
    return [];
  }

  return body.split(",").map((item) => parseScalar(item));
}

function stripComment(line) {
  let quoted = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];

    if (char === '"' && line[index - 1] !== "\\") {
      quoted = !quoted;
    }

    if (char === "#" && !quoted) {
      return line.slice(0, index);
    }
  }

  return line;
}

function parseTomlTables(text) {
  const tables = new Map();
  let current;

  for (const rawLine of text.split(/\r?\n/)) {
    const line = stripComment(rawLine).trim();

    if (!line) {
      continue;
    }

    const section = line.match(/^\[([^\]]+)]$/);

    if (section) {
      current = {};
      tables.set(section[1], current);
      continue;
    }

    const assignment = line.match(/^([A-Za-z0-9_-]+)\s*=\s*(.+)$/);

    if (!assignment || !current) {
      continue;
    }

    const [, key, value] = assignment;
    const trimmed = value.trim();

    current[key] = trimmed.startsWith("[") ? parseArray(trimmed) : parseScalar(trimmed);
  }

  return tables;
}

function parsePins(text) {
  const tables = parseTomlTables(text);
  const pins = new Map();

  for (const [name, values] of tables) {
    const revision = values.revision ?? values.rev ?? values.commit ?? values.pin;

    if (typeof revision === "string" && revision) {
      pins.set(name, revision);
    }
  }

  return pins;
}

function quote(value) {
  return JSON.stringify(value);
}

function renderLuaValue(value) {
  if (typeof value === "boolean") {
    return value ? "true" : "false";
  }

  if (Array.isArray(value)) {
    return `{ ${value.map((item) => quote(item)).join(", ")} }`;
  }

  return quote(value);
}

function renderParsers(parsers, pins) {
  const lines = [
    "-- Generated by scripts/update-registry.mjs from arborist-ts/arborist.nvim/registry.",
    "-- Do not edit manually.",
    "return {",
  ];

  for (const name of [...parsers.keys()].sort()) {
    const parser = parsers.get(name);

    if (!parser.url) {
      continue;
    }

    lines.push(`  [${quote(name)}] = {`);
    lines.push("    install_info = {");

    for (const key of ["url", "branch", "location", "generate"]) {
      if (parser[key] !== undefined) {
        lines.push(`      ${key} = ${renderLuaValue(parser[key])},`);
      }
    }

    const revision = pins.get(name);

    if (revision) {
      lines.push(`      revision = ${quote(revision)},`);
    }

    lines.push("    },");

    for (const key of ["maintainers", "requires", "readme_note"]) {
      if (parser[key] !== undefined) {
        lines.push(`    ${key} = ${renderLuaValue(parser[key])},`);
      }
    }

    lines.push("  },");
  }

  lines.push("}");
  lines.push("");

  return lines.join("\n");
}

function renderFiletypes(filetypes) {
  const lines = [
    "-- Generated by scripts/update-registry.mjs from arborist-ts/arborist.nvim/registry.",
    "-- Do not edit manually.",
    "local filetypes = {",
  ];

  for (const name of Object.keys(filetypes).sort()) {
    const aliases = filetypes[name];

    if (!Array.isArray(aliases) || aliases.length === 0) {
      continue;
    }

    lines.push(`  [${quote(name)}] = ${renderLuaValue(aliases)},`);
  }

  lines.push("}");
  lines.push("");
  lines.push("for lang, ft in pairs(filetypes) do");
  lines.push("  vim.treesitter.language.register(lang, ft)");
  lines.push("end");
  lines.push("");

  return lines.join("\n");
}

async function main() {
  const [parsersToml, pinsToml, filetypesToml] = await Promise.all([
    fetchText("parsers.toml"),
    fetchText("pins.toml"),
    fetchText("filetypes.toml"),
  ]);

  const parsers = parseTomlTables(parsersToml);
  const pins = parsePins(pinsToml);
  const filetypes = parseTomlTables(filetypesToml).get("filetypes") ?? {};

  await writeFile(path.join(repoRoot, "lua", "tiny-treesitter", "parsers.lua"), renderParsers(parsers, pins));
  await writeFile(path.join(repoRoot, "plugin", "filetypes.lua"), renderFiletypes(filetypes));

  log(`generated ${parsers.size} parser entries and ${Object.keys(filetypes).length} filetype mapping groups`);
}

await main();
